
function showMenu(){
    navMenu = document.getElementById('navMenu')
    navClose = document.getElementById('navClose')
    navMenu.style.display = 'block'
    navClose.style.display = 'block'
}

function removeMenu(){
    navMenu = document.getElementById('navMenu')
    navClose = document.getElementById('navClose')
    navMenu.style.display = 'none'
    navClose.style.display = 'none'
}

